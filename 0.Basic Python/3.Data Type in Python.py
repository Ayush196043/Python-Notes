# 1.Integer ( ke andere number aate ha +ve aor -ve dono aayte hai.)
#  Example:-
num = 10
print(num)
print("Data type of num ",type(num)) # type() function ka use karke ham variable ka data type print karte hai.

# Output: <class 'int'>

# 2.String ( ke andere santance aor name etc aayte hai.)
#  Example:-
Name = "Ayush Pandey"
print(Name)
print("Data type of Name ",type(Name)) # type() function ka use karke ham variable ka data type print karte hai.

# Output: <class 'str'>


# 3.Float ( ke andere number aate ha jo decimal aayte hai.)
#  Example:-
num = 10.5
print(num)
print("Data type of num ",type(num)) # type() function ka use karke ham variable ka data type print karte hai.

# Output: <class 'float'>

